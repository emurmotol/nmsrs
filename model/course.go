func (course *Course) Create() *Course {
	db := database.Conn()
	defer db.Close()

	if err := db.Create(&course).Error; err != nil {
		panic(err)
	}
	return course
}

func (course Course) Index(q string) []Course {
	db := database.Conn()
	defer db.Close()

	courses := []Course{}
	results := make(chan []Course)

	go func() {
		db.Find(&courses, "name LIKE ?", database.WrapLike(q))
		results <- courses
	}()

	courses = <-results
	close(results)
	return courses
}
